# ---------------------------------------------------------------------------
# Ключевые слова / фразы для нечёткого срабатывания бота.
# Совпадение ищется как ПОДСТРОКА в тексте (без учёта регистра), поэтому
# бот реагирует не только на точное слово, но и на однокоренные формы,
# опечатки и фразы вроде "и что там про меня".
# Дополняйте список по мере того, как видите новые формулировки в реальных
# комментариях и сообщениях.
# ---------------------------------------------------------------------------
TRIGGER_KEYWORDS = [
    # само слово и его формы/опечатки
    "послан",       # послание, пошли, послал(а) — покрывает основные формы
    "псослан",
    "посл ание",
    "poslanje",
    "псл",
    # смежные запросы про ожидание / очередь
    "жду",
    "ждю",
    "жд у",
    "когда до меня",
    "когда очередь",
    "очеред",
    "что там про меня",
    "и что там",
    "что же там",
    "про меня",
    # реакции при получении контента
    "здравствуй",
    "добрый вечер",
    "доброй ночи",
    "интересно",
    # подтверждение подписки/действия
    "подписал",
    "подписч",
    "готово",
    "считайте",
    "считаете",
    "попробуй",
    "благодарна",
    "благодарен",
]


def text_matches_trigger(text: str) -> bool:
    """
    Проверяет, содержит ли текст хотя бы одно ключевое слово/фразу
    из TRIGGER_KEYWORDS. Регистр не учитывается, совпадение — по подстроке,
    поэтому это нестрогое ("нечёткое") сравнение.
    """
    if not text:
        return False
    lowered = text.lower()
    return any(keyword in lowered for keyword in TRIGGER_KEYWORDS)


